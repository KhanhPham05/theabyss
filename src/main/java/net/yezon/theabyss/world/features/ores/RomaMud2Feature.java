import net.minecraft.data.worldgen.features.FeatureUtils;
import net.minecraft.core.Registry;
import net.minecraft.core.Holder;

import java.util.Set;
import java.util.Random;
import java.util.List;

public class RomaMud2Feature extends OreFeature {
	public static RomaMud2Feature FEATURE = null;
	public static Holder<ConfiguredFeature<OreConfiguration, ?>> CONFIGURED_FEATURE = null;
	public static Holder<PlacedFeature> PLACED_FEATURE = null;

	public static Feature<?> feature() {
		FEATURE = new RomaMud2Feature();
		CONFIGURED_FEATURE = FeatureUtils.register("theabyss:roma_mud_2", FEATURE,
				new OreConfiguration(RomaMud2FeatureRuleTest.INSTANCE, TheabyssModBlocks.ROMA_MUD_2.get().defaultBlockState(), 32));
		PLACED_FEATURE = PlacementUtils.register("theabyss:roma_mud_2", CONFIGURED_FEATURE, List.of(CountPlacement.of(32), InSquarePlacement.spread(),
				HeightRangePlacement.uniform(VerticalAnchor.absolute(40), VerticalAnchor.absolute(200)), BiomeFilter.biome()));
		return FEATURE;
	}

	public static Holder<PlacedFeature> placedFeature() {
		return PLACED_FEATURE;
	}

	public static final Set<ResourceLocation> GENERATE_BIOMES = Set.of(new ResourceLocation("theabyss:mud_plains"),
			new ResourceLocation("theabyss:blue_jungle"), new ResourceLocation("theabyss:blue_forest"));
	private final Set<ResourceKey<Level>> generate_dimensions = Set
			.of(ResourceKey.create(Registry.DIMENSION_REGISTRY, new ResourceLocation("theabyss:the_abyss")));

	public RomaMud2Feature() {
		super(OreConfiguration.CODEC);
	}

	public boolean place(FeaturePlaceContext<OreConfiguration> context) {
		WorldGenLevel world = context.level();
		if (!generate_dimensions.contains(world.getLevel().dimension()))
			return false;
		return super.place(context);
	}

	@Mod.EventBusSubscriber(bus = Mod.EventBusSubscriber.Bus.MOD)
	private static class RomaMud2FeatureRuleTest extends RuleTest {
		static final RomaMud2FeatureRuleTest INSTANCE = new RomaMud2FeatureRuleTest();
		private static final com.mojang.serialization.Codec<RomaMud2FeatureRuleTest> CODEC = com.mojang.serialization.Codec.unit(() -> INSTANCE);
		private static final RuleTestType<RomaMud2FeatureRuleTest> CUSTOM_MATCH = () -> CODEC;

		@SubscribeEvent
		public static void init(FMLCommonSetupEvent event) {
			Registry.register(Registry.RULE_TEST, new ResourceLocation("theabyss:roma_mud_2_match"), CUSTOM_MATCH);
		}

		private List<Block> base_blocks = null;

		public boolean test(BlockState blockAt, Random random) {
			if (base_blocks == null) {
				base_blocks = List.of(TheabyssModBlocks.ROMA_MUD.get());
			}
			return base_blocks.contains(blockAt.getBlock());
		}

		protected RuleTestType<?> getType() {
			return CUSTOM_MATCH;
		}
	}
}
